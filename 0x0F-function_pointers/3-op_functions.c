#include "3-calc.h"

/**
 * op_add - Function that adds 2 numbers.
 * @a: Number 1
 * @b: Number 2
 * Return: Sum
 */

int op_add(int a, int b)
{
	return (a + b);
}

/**
 * op_sub - Function that substracts 2 numbers.
 * @a: Number 1
 * @b: Number 2
 * Return: Diff
 */

int op_sub(int a, int b)
{
	return (a - b);
}

/**
 * op_mul - Function that multiplies 2 numbers.
 * @a: Number 1
 * @b: Number 2
 * Return: Mul
 */

int op_mul(int a, int b)
{
	return (a * b);
}

/**
 * op_div - Function that divides 2 numbers.
 * @a: Number 1
 * @b: Number 2
 * Return: Div
 */

int op_div(int a, int b)
{
	return (a / b);
}

/**
 * op_mod - Function that returns the reminder of the division of 2 numbers.
 * @a: Number 1
 * @b: Number 2
 * Return: Modulo
 */

int op_mod(int a, int b)
{
	return (a % b);
}
